package be.tusca.sample.swing.filter.inline.sample1;

public class Sample {

	private final String sample1;
	private final String sample2;
	private final String sample3;

	public Sample(String sample1, String sample2, String sample3) {
		this.sample1 = sample1;
		this.sample2 = sample2;
		this.sample3 = sample3;
	}

	public String getSample1() {
		return this.sample1;
	}

	public String getSample2() {
		return this.sample2;
	}

	public String getSample3() {
		return this.sample3;
	}


}
